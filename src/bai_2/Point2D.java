package bai_2;

import java.util.ArrayList;

public class Point2D {
    private float x;
    private  float y;
     private ArrayList<Point2D>XY;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public ArrayList<Point2D> getXY() {
        return XY;
    }

    public void setXY(ArrayList<Point2D> XY) {
        this.XY = XY;
    }
}
